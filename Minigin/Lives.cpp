Lives::Lives(GameObject* pGameObject)
	: Component(pGameObject)
	, m_pSubject{ std::make_unique<Subject>() }
{
}


//-----------------------------------------------------------------
// Destructor
//-----------------------------------------------------------------


//-----------------------------------------------------------------
// Public Member Functions
//-----------------------------------------------------------------
void Lives::Kill()
{
	--m_NrLives;

	m_pSubject->NotifyObservers(EventType::PLAYER_DIED, m_NrLives);
}


